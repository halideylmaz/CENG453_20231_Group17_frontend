package catan.ceng.catanui;

public class HexagonXY {
    private String resource;
    private int number;
    private double x;
    private double y;

    public HexagonXY(String resource, int number, double x, double y) {
        this.resource = resource;
        this.number = number;
        this.x = x;
        this.y = y;
    }
}